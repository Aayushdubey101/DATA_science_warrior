# Introduction to Python

"""
Python is a high-level, interpreted programming language known for its simplicity and readability.
It supports multiple programming paradigms, including procedural, object-oriented, and functional programming.

Comparison with Other Languages:
- Python vs Java: Python uses dynamic typing and has a simpler syntax, making it easier to write and read. Java is statically typed and often requires more boilerplate code.
- Python vs C++: Python manages memory automatically and is more beginner-friendly. C++ offers more control over system resources but is more complex.
- Python vs JavaScript: Python is commonly used for data science, automation, and backend development. JavaScript is primarily used for web development and runs in browsers.

Example Python code:
"""



print("Syntax to print 'Hello, World!' in different programming languages:\n")

print("Python:")
print('''\

    print("Hello, World!")

''')

print("Java:")
print('''\
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}
''')

print("C++:")
print('''\
#include <iostream>
using namespace std;

int main() {
    cout << "Hello, World!" << endl;
    return 0;
}
''')

print("JavaScript:")
print('''\
function greet() {
    console.log("Hello, World!");
}

greet();
''')
